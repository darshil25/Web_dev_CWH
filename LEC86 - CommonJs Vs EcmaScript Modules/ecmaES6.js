//there are 2 type of export:
//1. named export -> when you have multiple function or variable to export
//examples:

export const a = 1; 
export const b = 2;
export const c = 3;
export const d = 4;
export const e = 5;

//2. default export -> when you have only one function or variable to export
//example:
let obj;
export default obj = {
    a: 5,
    b: 7
}